<!-- print Character Boolean, Integer and Floating values using single variable.  -->
<?php
  $myVariable = 'a'; // Character
  echo $myVariable . '<br>';
  
  $myVariable = true; // Boolean
  echo $myVariable . '<br>';
  
  $myVariable = 123; // Integer
  echo $myVariable . '<br>';
  
  $myVariable = 3.14; // Floating-point number
  echo $myVariable . '<br>';
?>